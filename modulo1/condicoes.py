#  if /     elif     /   else
#  se    se não se      se não
entrada = input("Você quer entrar no sistema 'entrar' ou 'sair? ")

if entrada == "entrar":
    print("Você entrou no sistema")
elif entrada == "sair":
    print('Você  saiu do sistema')
else:
    print("Você não digitou nem entar nem sair")
    


